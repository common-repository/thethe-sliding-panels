  <h3>TheThe Sliding Panels - a WordPress plugin by TheThe Fly</h3>
<p align="justify">Our sliding panels - Top, Bottom, Left, Right  - as well as a Modal Window - are Javascript powered windows that float above the Web page. By providing sliding panels, you improve the usability of your website. Having to load pages over and over will annoy most users, so avoiding that is definitely a good thing. Sliding panels also allow you to save space by getting rid of large elements that do not need to be on the main page.</p>

<h4>Quick Usage Instructions</h4>

<p align="justify">Activating a sliding panel/modal window will cause activation of a widget area of the same name. For example, if you want to place any HTML code or widget into the <em>Top Sliding Panel</em>, open the <a href="/wp-admin/admin.php?page=thethe-sliding-panels&view=t" target="_blank"><em>Top Panel</em></a> folder, check the <em>Active</em> checkbox and set all the parameters you want. Then go to the <a href="/wp-admin/widgets.php" target="_blank"><em>Widgets</em></a> WP Admin section and put any widget into the <em>Top Sliding Panel</em> widget area. That's it!</p>

<p align="justify"><strong>Important! </strong>A sliding panel remains invisible as far as no widgets have been placed in.</p>
